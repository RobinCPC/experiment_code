"""
Simple math operating functions for unit test
"""


def add(a, b):
    """
    Adding to parameters and return result
    :param a:
    :param b:
    :return:
    """
    return a + b


def minus(a, b):
    """
    subtraction
    :param a:
    :param b:
    :return:
    """
    return a - b


def multi(a, b):
    """
    multiple
    :param a:
    :param b:
    :return:
    """
    return a * b


def divide(a, b):
    """
    division
    :param a:
    :param b:
    :return:
    """
    return a // b
